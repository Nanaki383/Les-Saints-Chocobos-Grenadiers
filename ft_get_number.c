int		ft_get_number(char *str)
{
	int i;
	int a;

	i = 0;
	a = 0;
	while (str[i] <= '9' && str[i] >= '0')
	{
		a = a * 10 + ((int)str[i] - 48);
		i++;
	}
	return (a);
}
