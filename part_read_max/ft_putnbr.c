#include "read.h"

void	ft_putnbr(int n)
{
	unsigned int nb;

	nb = n;
	if (nb > 2147483648 || n < 0)
	{
		ft_putchar('-');
		nb = nb * -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
